#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (!(s))
		return ;
	i = 0;
	while (s[i])
	{
		if (s[i] >= 32 && s[i] <= 127)
			write(fd, &s[i], 1);
		i++;
	}
}
